#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		a;

	if (n == -2147483648)
	{
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = (-1) * n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	if (n < 10)
	{
		a = n + '0';
		write(fd, &a, 1);
	}
}
